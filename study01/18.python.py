#__name__：python中内置变量，表示的当前模块的名字（直接运行当前模块，__name__的值为__main__；当该模块被导入时，__name__就是模块名）

#__all__是一个模块级别的特殊变量，用于指定from 模块名 import * 时会导入哪些功能（*通配了哪些功能）
