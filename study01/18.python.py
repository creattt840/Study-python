
#模块导入

#import 模块名   调用方式：模块名.功能名

#import 模块名 as 别名    调用方式：别名.功能名

#from 模块名 import 功能名    调用方式：功能名

#from 模块名 import 功能名 as 别名    调用方式：别名

#from 模块名 import *    调用方式：功能名


